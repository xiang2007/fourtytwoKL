#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	j;

	(void)argc;
	i = 0;
	j = 0;
	while (argv[i + 1])
	{
		write (1, argv[i + 1], 1);
		write (1, "\n", 1);
		i++;
	}
}
